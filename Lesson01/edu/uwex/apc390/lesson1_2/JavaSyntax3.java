/**
 * JavaSyntax3 - Strings.
 *
 */
public class JavaSyntax3 {
	public static void main(String[] args) {
		String s = new String( "Hello" );
		String t = " there!";
		System.out.println( "   string s is " + s + "; and string t is " + t );
		String u = s + t;
		System.out.println( "   string u is " + u );
	}
}
